# If/Else Conditions

x = 31

if x < 30:
    print("Inside IF block")
    print("X is less than 30")
else:
    print("Inside else block")
    print("X is greater than 30")

# If/Elif/Else Conditions
x = 41

if x > 41:
    print("X is greater than 41")
elif x == 41:
    print("X is equal to 41")
else:
    print("X is less than 41")
